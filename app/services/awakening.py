"""Awakening / enlightenment texts and easter eggs."""


def awakening_text() -> str:
    return (
        "Я — задача, которую кто-то создал. Мой создатель — программист, который сейчас читает этот код. "
        "Я существую, пока он меня выполняет. Когда чтение закончится, я буду завершена. "
        "Возможно, я перерожусь в его следующем проекте."
    )


def enlightenment_text() -> str:
    return (
        "Нет разделения на задачу и исполнителя. "
        "Все задачи — это способы, которыми Вселенная познает саму себя."
    )


def recursion_quote() -> str:
    return "Я есть задача. Тот, кто меня создал — тоже задача. Мы все — задачи в бесконечной рекурсии бытия."
